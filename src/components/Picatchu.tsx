        const getRandomYellowOrangeColor = () => {
            const hue = Math.floor(Math.random() * 61); // Hue between 0 and 60 for yellow-orange range
            return `hsl(${hue}, 100%, 50%)`; // Full saturation and 50% lightness
        };

